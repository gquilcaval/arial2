package model;

public class Caja {
	private int codCaja;
	private String nomCaja;
	public int getCodCaja() {
		return codCaja;
	}
	public void setCodCaja(int codCaja) {
		this.codCaja = codCaja;
	}
	public String getNomCaja() {
		return nomCaja;
	}
	public void setNomCaja(String nomCaja) {
		this.nomCaja = nomCaja;
	}

}
